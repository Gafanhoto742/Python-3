n1=int(input('Digite um valor:'))
n2=int(input('Digite outro:'))
soma=n1+n2
print('A soma entre {} e {} vale {}'.format(n1,n2,soma))
subt=n1-n2
print('A subtração entre {} e {} vale {}'.format(n1,n2,subt))
mult=n1*n2
print('A multiplicação entre {} e {} vale {}'.format(n1,n2,mult))
divs=n1/n2
print('A divisão entre {} e {} vale {}'.format(n1,n2,divs))
divis=n1//n2
print('A divisão inteira entre {} e {} vale {}'.format(n1,n2,divis))
rest=n1%n2
print('O resto da divisão entre {} e {} vale {}'.format(n1,n2,rest))
poten=n1**n2
print('A potencial entre {} e {} vale {}'.format(n1,n2,poten))
